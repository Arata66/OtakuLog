const STORAGE_KEY = 'otakulog-lang'
const lang = ref<Lang>((localStorage.getItem(STORAGE_KEY) as Lang) || 'zh')

export function useI18n() {
  function t(key: TranslationKey, ...params: (string | number)[]): string {
    let val: string = (translations[lang.value] || translations.zh)[key] || key
    params.forEach((p, i) => {
      val = val.replace(`{${i}}`, String(p))
    })
    return val
  }

  function setLang(newLang: Lang) {
    lang.value = newLang
    localStorage.setItem(STORAGE_KEY, newLang)
  }

  function toggleLang() {
    setLang(lang.value === 'zh' ? 'en' : 'zh')
  }

  const currentLang = computed(() => lang.value)

  return { t, setLang, toggleLang, currentLang, lang }
}
